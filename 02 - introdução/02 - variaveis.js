// Variaveis

// let é uma palavra reservada para criar variáveis,
// ou seja, um espaço na memória do computador para
// armazenar um valor.

let animal = "Cachorro"; // Declaração de uma variável chamada "animal" e atribuição do valor 'Cachorro'
console.log(animal); // Imprime o valor da variável "animal" no console

animal = "Gato"; // Atribuição de um novo valor 'Gato' à variável "animal"
console.log(animal); // Imprime o novo valor da variável "animal" no console

// const é uma palavra reservada para criar constantes,
// ou seja, um espaço na memória do computador para
// armazenar um valor que não pode ser alterado.
const pi = 3.14; // Declaração de uma constante chamada "pi" e atribuição do valor 3.14
console.log(pi); // Imprime o valor da constante "pi" no console

pi = 3.14159;
console.log(pi); // Tenta atribuir um novo valor à constante "pi" e imprime o resultado no console (isso causará um erro, pois constantes não podem ser reatribuídas)
